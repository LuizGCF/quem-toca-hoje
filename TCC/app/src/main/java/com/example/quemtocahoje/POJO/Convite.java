package com.example.quemtocahoje.POJO;


public class Convite {

    private Integer idConvite;
    private String emailConvidadoConvite;
    private String statusConvidadoConvite;
    private String tokenConvite;
    private String dataCriacao;
    private Integer idMusico;

    public Integer getIdConvite() {
        return idConvite;
    }

    public void setIdConvite(Integer idConvite) {
        this.idConvite = idConvite;
    }

    public String getEmailConvidadoConvite() {
        return emailConvidadoConvite;
    }

    public void setEmailConvidadoConvite(String emailConvidadoConvite) {
        this.emailConvidadoConvite = emailConvidadoConvite;
    }

    public String getStatusConvidadoConvite() {
        return statusConvidadoConvite;
    }

    public void setStatusConvidadoConvite(String statusConvidadoConvite) {
        this.statusConvidadoConvite = statusConvidadoConvite;
    }

    public String getTokenConvite() {
        return tokenConvite;
    }

    public void setTokenConvite(String tokenConvite) {
        this.tokenConvite = tokenConvite;
    }

    public String getDataCriacao() {
        return dataCriacao;
    }

    public void setDataCriacao(String dataCriacao) {
        this.dataCriacao = dataCriacao;
    }

    public Integer getIdMusico() {
        return idMusico;
    }

    public void setIdMusico(Integer idMusico) {
        this.idMusico = idMusico;
    }

}